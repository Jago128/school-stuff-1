package for_doWhile_WhileStuff;

public class ForProject9 {

	public static void main(String[] args) {
		int i;

		for (i = 1; i <= 10; i++) {
			if (i % 2 == 0) {
				System.out.println(i + " PAR");
			} else if (i == 5) {
				System.out.println("MITAD");
			} else {
				System.out.println(i);
			}
		}
	}
}
